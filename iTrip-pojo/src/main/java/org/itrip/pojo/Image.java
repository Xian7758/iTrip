package org.itrip.pojo;

public class Image {
	private int id;
	private String img;
	private int htid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getHtid() {
		return htid;
	}
	public void setHtid(int htid) {
		this.htid = htid;
	}
	
	
}
